function makeBaseSummaryTable(extracted_data, model_set, root, base_ind)

varnames_base_summary = {'Simulation time','wake loss factor (mV/pC)', 'wake length (m)', 'mesh stepsize (um)', 'timestep (fs)',...
    'beam port loss (%)', 'signal port loss (%)', 'structure loss (%)',...
    'number of mesh cells (million)', 'memory used (MB)', 'number of cores'};
base_sim_time_string = sec_to_time_string(extracted_data.simulation_time(base_ind));
base_mesh_stepsize = extracted_data.mesh_density(base_ind) ./ extracted_data.mesh_scaling(base_ind) .* 1e6;
T_base_summary = table(base_sim_time_string, ...
    round(extracted_data.wlf(base_ind) .* 1E-9),...
    extracted_data.wake_length(base_ind), ...
    base_mesh_stepsize,...
    round(extracted_data.timestep(base_ind) .* 1E15),...
    round(extracted_data.beam_port_loss(base_ind) .* 100),...
    round(extracted_data.signal_port_loss(base_ind) .* 100),...
    round(extracted_data.structure_loss(base_ind) .* 100),...
    round(extracted_data.number_of_cells(base_ind)  ./ 1E6),...
    extracted_data.memory_usage(base_ind),...
    extracted_data.n_cores(base_ind), ...
    'VariableNames', varnames_base_summary);
writetable(T_base_summary, fullfile(root, model_set,...
    [model_set, '_base_summary.txt']), ...
    'Delimiter','|',...
    'WriteVariableNames',true, 'WriteRowNames',false)



